package com.training.springbootlearn;

public class XmlBean {
	
	private String name;
	
	private XmlChild child;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public XmlChild getChild() {
		return child;
	}

	public void setChild(XmlChild child) {
		this.child = child;
	}
	

}
